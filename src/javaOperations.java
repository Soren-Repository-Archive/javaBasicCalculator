public class javaOperations {

    public javaOperations() {}

    public double calAdd(double x, double y) { double z = x + y; return z; }

    public double calMinus(double x, double y) { double k = x - y; return k; }

    public double calMultiply(double x, double y) { double l = x * y; return l; }

    public double calDivide(double x, double y){ double p = x / y; return p; }
}
